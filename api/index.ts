function getSimulatedAIResponse(message: string, appStateContext?: string): string {
  const baseResponse = getRawSimulatedAIResponse(message, appStateContext);
  if (baseResponse.trim().startsWith("{") || baseResponse.trim().startsWith("[")) {
    return baseResponse; // Skip modifying simulation data payloads
  }

  // Prepend a dynamic analysis block for extreme flexibility based on user intent / sent message
  let dynamicAnalysis = "";
  const lowercaseMsg = message.toLowerCase();
  
  // Extract custom traits dynamically
  let detectedTraits: string[] = [];
  if (lowercaseMsg.includes("بخيل") || lowercaseMsg.includes("حريص") || lowercaseMsg.includes("بيفاصل") || lowercaseMsg.includes("فصال") || lowercaseMsg.includes("شحيح") || lowercaseMsg.includes("ملاوز")) {
    detectedTraits.push("التوفير الشديد والحذر المالي المرتفع (فصال وبخل)");
  }
  if (lowercaseMsg.includes("غاضب") || lowercaseMsg.includes("معترض") || lowercaseMsg.includes("زعلان") || lowercaseMsg.includes("اشتك") || lowercaseMsg.includes("منفعل") || lowercaseMsg.includes("متعصب")) {
    detectedTraits.push("الاعتراض والغضب (بحاجة لامتصاص الشحنة النفسية أولاً)");
  }
  if (lowercaseMsg.includes("متردد") || lowercaseMsg.includes("خايف") || lowercaseMsg.includes("قلق") || lowercaseMsg.includes("قلقان")) {
    detectedTraits.push("التردد والخوف من تقلبات السوق (بحاجة لبناء الثقة والضمانات)");
  }
  if (lowercaseMsg.includes("جديد") || lowercaseMsg.includes("أول تعامل") || lowercaseMsg.includes("كشاف") || lowercaseMsg.includes("أول مرة")) {
    detectedTraits.push("عميل جديد (بحاجة لتسهيلات تشجيعية وكمية مصغرة لكسر القلق)");
  }
  if (lowercaseMsg.includes("قديم") || lowercaseMsg.includes("مستمر") || lowercaseMsg.includes("سابق") || lowercaseMsg.includes("زبوون")) {
    detectedTraits.push("عميل قديم ودائم (بحاجة لتقدير كرامته المهنية والولاء)");
  }
  if (lowercaseMsg.includes("مماطل") || lowercaseMsg.includes("مطنش") || lowercaseMsg.includes("تأخير") || lowercaseMsg.includes("مأخر") || lowercaseMsg.includes("تأخر")) {
    detectedTraits.push("المماطلة وتأخير السداد (بحاجة لحزم تفاوضي وربط التوريد بالتحصيل)");
  }

  let customerNameInQuery = "";
  const nameRegex = /(?:ماركت|سوبرماركت|سوبر ماركت|محل|العميل|التاجر|أبو)\s+([^\s]+)/i;
  const match = message.match(nameRegex);
  if (match && match[0]) {
    customerNameInQuery = match[0];
  } else {
    const namesList = ["أحمد", "محمد", "محمود", "علي", "سعيد", "سليمان", "جمال", "رجب", "العشري", "النمر"];
    for (const name of namesList) {
      if (message.includes(name)) {
        customerNameInQuery = `المعلم ${name}`;
        break;
      }
    }
  }
  const targetName = customerNameInQuery || "العميل";
  
  let detectedType = "محل عام / بقالة تجزئة";
  if (lowercaseMsg.includes("فرن") || lowercaseMsg.includes("مخبز")) detectedType = "فرن ومخبز بلدي/إفرنجي (B2B)";
  else if (lowercaseMsg.includes("هايبر")) detectedType = "هايبر ماركت / سلسلة تجارية كبرى";
  else if (lowercaseMsg.includes("سوبرماركت") || lowercaseMsg.includes("سوبر ماركت")) detectedType = "سوبر ماركت متوسط/كبير";
  else if (lowercaseMsg.includes("مطعم") || lowercaseMsg.includes("مطاعم")) detectedType = "مطعم مأكولات / تجهيزات غذائية";
  
  let detectedLocation = "غير محدد بدقة (ميداني)";
  const locationKeywords = ["الزقازيق", "ميت غمر", "بلبيس", "بدر", "القاهرة", "الجيزة", "الإسكندرية", "دمنهور", "المنصورة", "العاشر"];
  for (const loc of locationKeywords) {
    if (message.includes(loc)) {
      detectedLocation = loc;
      break;
    }
  }

  if (detectedTraits.length > 0 || detectedType !== "محل عام / بقالة تجزئة" || detectedLocation !== "غير محدد بدقة (ميداني)") {
    dynamicAnalysis = `🎯 **[تحليل فوري وحركي للرسالة المرسلة حالياً - مستشار مبيعات EAGS]**
- **العميل المستهدف**: ${targetName}
- **صفات العميل المكتشفة بالرسالة**: ${detectedTraits.join(" و ") || "الاستفسار والتحري المباشر"}
- **نوع النشاط الميداني**: ${detectedType}
- **النطاق الجغرافي المستنبط**: ${detectedLocation}

📢 *توجيه تفاوضي تكتيكي مخصص لهذه الرسالة الراهنة*:
بناءً على تفاصيل الموقف المذكور في رسالتك الراهنة (وليس على القوالب الجامدة المخزنة مسبقاً)، يرجى استخدام السيناريو التالي للتفاوض الذكي وسرعة إتمام البيع كاش:

👈 *"يا معلمنا ${targetName === 'العميل' ? 'المحترم' : targetName}، أنا جيتلك مخصوص النهاردة في ${detectedLocation === 'غير محدد بدقة (ميداني)' ? 'منطقتك الجميلة ودوران بضاعتنا سريع والطلب عليها عالي جداً' : detectedLocation} وبضاعتنا طازة وخام ممتاز. كشريك نجاح، أنا رتبتلك موازنة وخصومات ممتازة جداً تفيدك وتسهلك البيع الميداني اليوم بسداد كاش فوري يضمن حركتك!"*
__________________________________________________\n\n`;
  }

  return dynamicAnalysis + baseResponse;
}


app.post("/api/gemini/chat", async (req: any, res: any) => {
  try {
    const { systemInstruction, history, message } = req.body;
    const key = process.env.GEMINI_API_KEY;
    
    if (!key || key.trim() === "" || key.includes("YOUR_API_KEY")) {
      const text = getSimulatedAIResponse(message);
      return res.json({ text });
    }

    const ai = new GoogleGenAI({
      apiKey: key,
      httpOptions: { headers: { 'User-Agent': 'aistudio-build' } }
    });

    let contents: any[] = [];
    if (history && Array.isArray(history)) {
      contents = history.map((item: any) => ({
        role: item.role,
        parts: [{ text: item.text }]
      }));
    }
    contents.push({ role: "user", parts: [{ text: message }] });

    const response = await ai.models.generateContent({
      model: "gemini-2.5-flash",
      contents,
      config: {
        systemInstruction: systemInstruction || "You are a helpful assistant.",
        tools: [{ googleSearch: {} }]
      }
    });
    
    let responseText = response.text || '';
    
    const groundingChunks = response.candidates?.[0]?.groundingMetadata?.groundingChunks || [];
    const sources = groundingChunks
      .filter((chunk: any) => chunk.web && chunk.web.uri)
      .map((chunk: any) => ({
        title: chunk.web.title || "مصدر خارجي",
        uri: chunk.web.uri
      }));

    if (sources.length > 0) {
      responseText += '\n\n**مصادر البحث (جوجل):**\n';
      const uniqueSources = Array.from(new Map(sources.map((item: any) => [item.uri, item])).values()) as any[];
      uniqueSources.forEach((source: any, idx: number) => {
        responseText += `${idx + 1}. [${source.title}](${source.uri})\n`;
      });
    }
    
    res.json({ text: responseText });
  } catch (error: any) {
    const text = getSimulatedAIResponse(req.body.message);
    res.json({ text });
  }
});

