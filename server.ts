        return `*"يا معلمنا المحترم، أنا مقدر تماماً حرصك وحساباتك الدقيقة للقرش، وده حقك كرجل أستاذ في السوق! إحنا شركاء حقيقيين ونجاح محلك وزيادة أرباحك هي هدفنا الأول. تيسيراً لك اليوم على وضع السوق المتقلب والمنافسة، أنا رتبت لك موازنة وخصومات ممتازة جداً تفيدك، وبحسبة بسيطة العبوة هتقف عليك بأقل تكلفة وهتبيعها بهامش ربح سريع يرجعلك كاش فوري وسرعة دوران تدمر أي ركود!"*`;
